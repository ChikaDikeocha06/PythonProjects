#Example of Multiple Parameters
def add_numbers(x, y):        # (4) multiple parameters
    result = x + y           # (2) computation
    print(result)            # (2) output

add_numbers(3, 5)            # (5) arguments
#def= the keyword for functions
#add_numbers= the function variable
#(x,y)= the parameters to the variable
#result= the local varaiable
#3,5 is the arguemnts/ values to the paramets in the function
