#Example of a range(start, stop, step) loop
for i in range(0,10,2):
    print(i)
# for, in is the keyword for looping
# i is the loop variable that holds the values of the range assigned in the loop
#range is a function used to give values from 1-9 but are even (counting by 2)
# print is a funtion that displays outputs to user

