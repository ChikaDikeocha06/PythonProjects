#Example of Return Values (the ouput)
def add_good(x, y):          # (1) header
    total = x + y            # (2) body
    return total             # (6) return value

result = add_good(3, 5)      # (7) storing return
print(result)
#def= the keyword to define function
#add_good= the function variable
#(x,y) is the function's parameters
#total= the local variable which equals the value of two int values added
#return total= sends the value back
#results= stores returned value
#print(result)= displays the variable result's value