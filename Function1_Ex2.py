#Example of a function with Parameters (the input)
def greet(name):                 # (1) header + (4) parameter
    print(f"Hello, {name}!")     # (2) body using parameter

greet("Bob")                     # (5) argument
#def= the keyword to define a function
#greet= the function name
#name= the parameter (input variable)
#"Bob"= the argument (the value passed in by the coder)