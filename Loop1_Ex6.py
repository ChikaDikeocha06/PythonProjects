#Example of looping with Mixed Types
for item in [42, "hello", 3.14,True]:
    print(item, type(item)) 
#for in is keywords for looping
# item is the loop variable and is used to store each value in the list
#[42, "hello", 3.14, True] is the list 
# print function is used to display output to user
# type() function is used to print the data type of the argement in the parentheses