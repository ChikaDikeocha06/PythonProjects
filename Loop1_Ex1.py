#Example: Printing cookie prices using a loop

#A loop that runs from 1 to 5
for cookie_number in range(1, 6): #range is 1-5 because it stops at 6
    print(f"Cookie {cookie_number}: $2.50") #prints each cookie number

# cookie_number is a variable and is used to store the current # in each loop
# range(1, 6) is a function used to generate numbers 1-5 for looping
# print() is a funtion used to display output to user
# for, in are keywords used to create a loo that reapeats code
# f"..." is a formatted string used to instert variables into text